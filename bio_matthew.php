                <p class="bio_paragraph">
                    Matthew Berland is an Assistant Professor of Digital Media in the
                    Department of Curriculum & Instruction at University of Wisconsin-Madison.
                    He studies how to support computational thinking and learning though
                    constructionist learning environments (games, makerspaces) and how to
                    understand that learning by using mixed methods, educational data mining,
                    and learning analytics. In addition, Matthew is a Discovery Fellow of
                    the Wisconsin Institute for Discovery. He received his Ph.D. in
                    Learning Sciences (with foci in computer science and cognitive science)
                    from Northwestern University in 2008, studying computational literacy,
                    complex systems literacy, and the design of constructionist learning
                    environments. In 2009, he completed a postdoctoral fellowship in the
                    Institute for Computational Engineering and Sciences at the University
                    of Texas at Austin working on artificial intelligence (HRI). Matthew is
                    a graduate of Brown University, where he concentrated in Computer Science
                    (AI) and Modern Culture & Media (Semiotics). He has been employed, variously,
                    as a high school teacher, a middle school teacher, a software developer, a
                    senior software engineer, a film projectionist, and an undergraduate chemistry
                    tutor. He is the director of the Complex Play Lab, and his current projects
                    include an NSF-funded handheld embodied virtual robotics game to study how
                    novices learn to program; a computational thinking project using designer 
                    abletop board games; an NSF-funded project to investigate the learning processes
                    of novice makers in the New York Hall of Science; a Microsoft-funded project to
                    create a novel curriculum to teach computational thinking and game design thinking;
                    and novel assessments for constructionist classrooms and informal learning spaces.
